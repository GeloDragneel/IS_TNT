            $setup->profit_prcnt_a = $profitA === '' ? 0 : $profitA;
            $setup->profit_prcnt_b = $profitB === '' ? 0 : $profitB;
            $setup->profit_prcnt_c = $profitC === '' ? 0 : $profitC;
            $setup->save();
        }
        return $priceSetups;
