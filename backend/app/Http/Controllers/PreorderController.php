    public function getUnsoldOrdersByProductId($productId)
    {
        $poQuery = DB::table('t_porder_master as A')
            ->join('t_porder_detail as B', 'B.po_number', '=', 'A.po_number')
            ->join('m_products as C', 'C.id', '=', 'B.product_id')
            ->select([
